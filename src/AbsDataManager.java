public class AbsDataManager {

    public int price;
    public int needed_sugar;
    public int needed_creamer;
    public int needed_coffeeBeans;
    public int needed_yulmu;
    public int needed_cocoa;




    AbsDataManager(int price, int sugar, int beans, int creamer, int yulmu, int cocoa) {
        this.price = price;
        this.needed_sugar = sugar;
        this.needed_creamer = creamer;
        this.needed_coffeeBeans = beans;
        this.needed_yulmu = yulmu;
        this.needed_cocoa = cocoa;
    }


}
